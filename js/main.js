function max (arg1, arg2) {
    var max = Math.max (arg1, arg2)
    return max;
}


function min (arg1, arg2) {
    var min = Math.min (arg1, arg2)
    return min;
}


function qwad (a, n) {
    var qwad = Math.pow (a, n)
    return qwad;
}

console.log(max(3, 6));

console.log(min(3, 6));

console.log(qwad(3, 6))